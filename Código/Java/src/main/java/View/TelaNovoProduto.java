        private void novo_produto(){
        String sql = "insert into produtos (nome_produto, marca, preco) values (?, ?, ?)";
            try{
                pst=conexao.prepareStatement(sql);
                pst.setString(1,produto.getText());
                pst.setString(2, preco.getText());
                pst.setString(3, marca.getText());

                
                int adicionado = pst.executeUpdate();
                if (adicionado >0){
                    JOptionPane.showMessageDialog(null, "Produto adicionado com sucesso");
                    produto.setText(null);
                    preco.setText(null);
                    marca.setText(null);

                }
                
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Erro ao salvar dados" + e.toString());
            }
        }
